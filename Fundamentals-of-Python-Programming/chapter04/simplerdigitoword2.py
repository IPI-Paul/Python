# Use a multi-way conditional statement
value = int(input("Please enter an integer in the range 0...5: "))
if value == 0:
    print("zero")
if value == 1:
    print("one")
if value == 2:
    print("two")
if value == 3:
    print("three")
if value == 4:
    print("four")
if value == 5:
    print("five")
print("Done")